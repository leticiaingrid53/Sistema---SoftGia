public class Extra {

     private int Codigo_Servico;
    private String Nome_Parceria;
    private String Acordo_Servico;
    private String Descricao_Servico;
    private Double Valor_Servico;
    
    public int getCodigo_Servico() {
        return Codigo_Servico;
    }

    public void setCodigo_Servico(int Codigo_Servico) {
        this.Codigo_Servico = Codigo_Servico;
    }

    public String getNome_Parceria() {
        return Nome_Parceria;
    }

    public void setNome_Parceria(String Nome_Parceria) {
        this.Nome_Parceria = Nome_Parceria;
    }

    public String getAcordo_Servico() {
        return Acordo_Servico;
    }

    public void setAcordo_Servico(String Acordo_Servico) {
        this.Acordo_Servico = Acordo_Servico;
    }

    public String getDescricao_Servico() {
        return Descricao_Servico;
    }

    public void setDescricao_Servico(String Descricao_Servico) {
        this.Descricao_Servico = Descricao_Servico;
    }

    public Double getValor_Servico() {
        return Valor_Servico;
    }

    public void setValor_Servico(Double Valor_Servico) {
        this.Valor_Servico = Valor_Servico;
    }
   
}
