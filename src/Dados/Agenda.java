public class Agenda {
 private int Cod_Agenda;
    private String Data_Agenda;
    private String Hora_Agenda;
    private String Local_Servico;
    private String Descricao_Servico;
    
    public int getCod_Agenda() {
        return Cod_Agenda;
    }

    public void setCod_Agenda(int Cod_Agenda) {
        this.Cod_Agenda = Cod_Agenda;
    }

    public String getData_Agenda() {
        return Data_Agenda;
    }

    public void setData_Agenda(String Data_Agenda) {
        this.Data_Agenda = Data_Agenda;
    }

    public String getHora_Agenda() {
        return Hora_Agenda;
    }

    public void setHora_Agenda(String Hora_Agenda) {
        this.Hora_Agenda = Hora_Agenda;
    }

    public String getLocal_Servico() {
        return Local_Servico;
    }

    public void setLocal_Servico(String Local_Servico) {
        this.Local_Servico = Local_Servico;
    }

    public String getDescricao_Servico() {
        return Descricao_Servico;
    }

    public void setDescricao_Servico(String Descricao_Servico) {
        this.Descricao_Servico = Descricao_Servico;
    }
   
    
    
}
